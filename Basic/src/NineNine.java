
public class NineNine {

	public static void main(String[] args) {
		for (int j = 1; j <= 9; j++) {
			for (int i = 1; i <= 9; i++) {
				System.out.print(i + " * " + j + " = " + i * j + "\t");
			}
			System.out.print("\n");
		} //end of outer for
	} // end of main()
} // end of class NineNine
